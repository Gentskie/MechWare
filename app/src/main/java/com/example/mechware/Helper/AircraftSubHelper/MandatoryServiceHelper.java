package com.example.mechware.Helper.AircraftSubHelper;

public class MandatoryServiceHelper {

    public String date, total_time, mandatory_service;

    public MandatoryServiceHelper() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTotal_time() {
        return total_time;
    }

    public void setTotal_time(String total_time) {
        this.total_time = total_time;
    }

    public String getMandatory_service() {
        return mandatory_service;
    }

    public void setMandatory_service(String mandatory_service) {
        this.mandatory_service = mandatory_service;
    }
}
