package com.example.mechware.Helper.AircraftSubHelper;

public class DescriptionHelper {
    public String date, tach_time, todays_flight, total_time, description;

    public DescriptionHelper() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTach_time() {
        return tach_time;
    }

    public void setTach_time(String tach_time) {
        this.tach_time = tach_time;
    }

    public String getTodays_flight() {
        return todays_flight;
    }

    public void setTodays_flight(String todays_flight) {
        this.todays_flight = todays_flight;
    }

    public String getTotal_time() {
        return total_time;
    }

    public void setTotal_time(String total_time) {
        this.total_time = total_time;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
