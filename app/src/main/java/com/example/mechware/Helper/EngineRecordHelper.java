package com.example.mechware.Helper;

public class EngineRecordHelper {

    public String manufacturer, model, serial, engine_belongs_to, min_octane_fuel, summer, winter, magneto_time,
    point_setting, firing_order, spark_plug_gap, recommended_overhaul, uid;

    public EngineRecordHelper() {
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getSerial() {
        return serial;
    }

    public void setSerial(String serial) {
        this.serial = serial;
    }

    public String getEngine_belongs_to() {
        return engine_belongs_to;
    }

    public void setEngine_belongs_to(String engine_belongs_to) {
        this.engine_belongs_to = engine_belongs_to;
    }

    public String getMin_octane_fuel() {
        return min_octane_fuel;
    }

    public void setMin_octane_fuel(String min_octane_fuel) {
        this.min_octane_fuel = min_octane_fuel;
    }

    public String getSummer() {
        return summer;
    }

    public void setSummer(String summer) {
        this.summer = summer;
    }

    public String getWinter() {
        return winter;
    }

    public void setWinter(String winter) {
        this.winter = winter;
    }

    public String getMagneto_time() {
        return magneto_time;
    }

    public void setMagneto_time(String magneto_time) {
        this.magneto_time = magneto_time;
    }

    public String getPoint_setting() {
        return point_setting;
    }

    public void setPoint_setting(String point_setting) {
        this.point_setting = point_setting;
    }

    public String getFiring_order() {
        return firing_order;
    }

    public void setFiring_order(String firing_order) {
        this.firing_order = firing_order;
    }

    public String getSpark_plug_gap() {
        return spark_plug_gap;
    }

    public void setSpark_plug_gap(String spark_plug_gap) {
        this.spark_plug_gap = spark_plug_gap;
    }

    public String getRecommended_overhaul() {
        return recommended_overhaul;
    }

    public void setRecommended_overhaul(String recommended_overhaul) {
        this.recommended_overhaul = recommended_overhaul;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}
