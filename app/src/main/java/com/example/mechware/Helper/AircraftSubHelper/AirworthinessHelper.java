package com.example.mechware.Helper.AircraftSubHelper;

public class AirworthinessHelper {
    public String date, ad_number, total_time, airworthiness_directive;

    public AirworthinessHelper() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getAd_number() {
        return ad_number;
    }

    public void setAd_number(String ad_number) {
        this.ad_number = ad_number;
    }

    public String getTotal_time() {
        return total_time;
    }

    public void setTotal_time(String total_time) {
        this.total_time = total_time;
    }

    public String getAirworthiness_directive() {
        return airworthiness_directive;
    }

    public void setAirworthiness_directive(String airworthiness_directive) {
        this.airworthiness_directive = airworthiness_directive;
    }
}
